//! Composable wrapper for parsing Anchor events alongside regular instructions.
//!
//! Anchor programs emit events two ways:
//! 1. **"Program data:" logs** — base64-encoded event data in transaction logs
//! 2. **Self-CPI** — inner instruction with an 8-byte `EVENT_IX_TAG` prefix
//!
//! This crate provides [`AnchorEventInstructionParser`] which wraps an existing
//! instruction parser and an event parser (both generated by `include_vixen_parser!`)
//! to handle both patterns automatically.
//!
//! # Example
//!
//! ```rust, ignore
//! include_vixen_parser!("idls/pump_fun.json");
//! include_vixen_parser!("idls/pump_fun_events.json");
//!
//! let parser = AnchorEventInstructionParser::new(
//!     pump_fun::InstructionParser,
//!     pump_fun_events::InstructionParser,
//!     pump_fun::PROGRAM_ID,
//! );
//! ```

use std::{borrow::Cow, future::Future, sync::Arc};

use yellowstone_vixen_core::{
    instruction::{InstructionUpdate, Path},
    ParseError, ParseResult, Parser, Prefilter, ProgramParser, Pubkey,
};

#[cfg(feature = "proto")]
mod proto_schema;

#[cfg(feature = "proto")]
pub use proto_schema::merge_proto_schemas;

/// 8-byte Anchor self-CPI event prefix.
///
/// When an Anchor program emits an event via self-CPI, the instruction data
/// starts with these 8 bytes, followed by the event discriminator and borsh payload.
///
/// Value: first 8 bytes of `sha256("anchor:event")`, stored as little-endian u64.
/// Defined in [`anchor-lang/src/event.rs`](https://github.com/coral-xyz/anchor/blob/v0.30.1/lang/src/event.rs#L55).
pub const EVENT_IX_TAG: [u8; 8] = 0x1d9a_cb51_2ea5_45e4_u64.to_le_bytes();

/// Combined output from instruction + event parsing.
#[derive(Debug, PartialEq)]
pub struct AnchorEventOutput<InstructionOut, EventOut> {
    /// Parsed instruction (None if this was a CPI event or filtered).
    pub instruction: Option<InstructionOut>,
    /// Events parsed from logs and/or CPI self-invocations.
    pub anchor_events: Vec<EventOut>,
}

#[cfg(feature = "proto")]
impl<InstructionOut, EventOut> prost::Message for AnchorEventOutput<InstructionOut, EventOut>
where
    InstructionOut: prost::Message,
    EventOut: prost::Message,
{
    fn encode_raw(&self, buf: &mut impl prost::bytes::BufMut) {
        if let Some(ref ix) = self.instruction {
            prost::encoding::message::encode(1, ix, buf);
        }

        for event in &self.anchor_events {
            prost::encoding::message::encode(2, event, buf);
        }
    }

    fn merge_field(
        &mut self,
        _tag: u32,
        _wire_type: prost::encoding::WireType,
        _buf: &mut impl prost::bytes::Buf,
        _ctx: prost::encoding::DecodeContext,
    ) -> Result<(), prost::DecodeError> {
        // AnchorEventOutput is encode-only. Decoding from proto is not supported
        // because the inner types (Instructions) do not implement Default.
        Err(prost::DecodeError::new(
            "AnchorEventOutput does not support proto decoding",
        ))
    }

    fn encoded_len(&self) -> usize {
        let mut len = 0;

        if let Some(ref ix) = self.instruction {
            len += prost::encoding::message::encoded_len(1, ix);
        }

        for event in &self.anchor_events {
            len += prost::encoding::message::encoded_len(2, event);
        }

        len
    }

    fn clear(&mut self) {
        self.instruction = None;

        self.anchor_events.clear();
    }
}

#[cfg(feature = "proto")]
impl<InstructionOut, EventOut> Default for AnchorEventOutput<InstructionOut, EventOut> {
    fn default() -> Self {
        Self {
            instruction: None,
            anchor_events: Vec::new(),
        }
    }
}

/// Composable parser that wraps an instruction parser and an event parser.
///
/// Automatically detects CPI events (by `EVENT_IX_TAG` prefix) and log events
/// (by scanning `"Program data: "` lines), routing each to the appropriate parser.
#[derive(Debug, Clone, Copy)]
pub struct AnchorEventInstructionParser<InstructionParser, EventParser> {
    instruction_parser: InstructionParser,
    event_parser: EventParser,
    program_id: Pubkey,
}

impl<InstructionParser, EventParser> AnchorEventInstructionParser<InstructionParser, EventParser> {
    /// Create a new composable parser.
    ///
    /// - `instruction_parser`: parses regular instructions (from the program's instruction IDL)
    /// - `event_parser`: parses events (from the program's events IDL, where events
    ///   are modeled as instructions with discriminators at offset 8)
    /// - `program_id`: the program's public key, used to detect self-CPI events
    #[must_use]
    pub fn new(
        instruction_parser: InstructionParser,
        event_parser: EventParser,
        program_id: [u8; 32],
    ) -> Self {
        Self {
            instruction_parser,
            event_parser,
            program_id: Pubkey::new(program_id),
        }
    }
}

fn is_anchor_cpi_event(data: &[u8]) -> bool { data.len() >= 8 && data[..8] == EVENT_IX_TAG }

/// Extract event payloads from "Program data: " log lines.
///
/// For each matching line, base64-decodes the payload and prepends `EVENT_IX_TAG`
/// so the data layout matches what the events parser expects (discriminator at offset 8).
fn extract_log_event_payloads(logs: &[String]) -> Vec<Vec<u8>> {
    const PREFIX: &str = "Program data: ";

    logs.iter()
        .filter_map(|line| {
            let encoded = line.strip_prefix(PREFIX)?;

            let decoded =
                base64::Engine::decode(&base64::engine::general_purpose::STANDARD, encoded.trim())
                    .ok()?;

            // Prepend EVENT_IX_TAG so the discriminator sits at offset 8,
            // matching the layout the events parser expects from CPI data.
            let mut data = Vec::with_capacity(EVENT_IX_TAG.len() + decoded.len());

            data.extend_from_slice(&EVENT_IX_TAG);
            data.extend_from_slice(&decoded);

            Some(data)
        })
        .collect()
}

/// Build a synthetic [`InstructionUpdate`] to feed log-based event data to the
/// event parser.
///
/// Log events ("Program data:" lines) are not real instructions — they are just
/// base64 payloads embedded in transaction logs. To reuse the same event parser
/// that handles CPI self-invocation events, we wrap the decoded payload in a
/// fake `InstructionUpdate` with empty accounts and no inner instructions.
/// The `data` field layout matches a real CPI event instruction:
///
/// ```text
/// [ EVENT_IX_TAG (8 bytes) | discriminator (8 bytes) | borsh payload ]
/// ```
fn synthetic_instruction(
    program_id: Pubkey,
    data: Vec<u8>,
    shared: &Arc<yellowstone_vixen_core::instruction::InstructionShared>,
    path: &Path,
) -> InstructionUpdate {
    InstructionUpdate {
        program: program_id,
        accounts: vec![],
        data,
        shared: Arc::clone(shared),
        inner: vec![],
        path: path.clone(),
        log_range: 0..0,
    }
}

macro_rules! impl_parser {
    ($($extra_bound:tt)*) => {
        fn id(&self) -> Cow<'static, str> { self.instruction_parser.id() }

        fn prefilter(&self) -> Prefilter {
            let mut prefilter = self.instruction_parser.prefilter();

            prefilter.merge(self.event_parser.prefilter());

            prefilter
        }

        fn parse(
            &self,
            value: &Self::Input,
        ) -> impl Future<Output = ParseResult<Self::Output>> + Send {
            async move {
                // CPI event as a standalone instruction — filtered because the
                // parent instruction's parse call already collects it.
                if is_anchor_cpi_event(&value.data) && value.program == self.program_id {
                    return Err(ParseError::Filtered);
                }

                // 1. Regular instruction: delegate to the instruction parser.
                let ix_result = match self.instruction_parser.parse(value).await {
                    Ok(ix) => Some(ix),
                    Err(ParseError::Filtered) => None,
                    Err(e) => return Err(e),
                };

                let mut anchor_events = Vec::new();

                // 2. Scan inner instructions for CPI self-invocation events.
                for inner in &value.inner {
                    if is_anchor_cpi_event(&inner.data) && inner.program == self.program_id {
                        match self.event_parser.parse(inner).await {
                            Ok(event) => anchor_events.push(event),
                            Err(ParseError::Filtered) => continue,
                            Err(e) => return Err(e),
                        }
                    }
                }

                // 3. Scan logs for "Program data:" events.
                let log_payloads = extract_log_event_payloads(value.log_messages());

                for payload in log_payloads {
                    let synthetic =
                        synthetic_instruction(self.program_id, payload, &value.shared, &value.path);

                    match self.event_parser.parse(&synthetic).await {
                        Ok(event) => anchor_events.push(event),
                        Err(ParseError::Filtered) => continue,
                        Err(e) => return Err(e),
                    }
                }

                // If we got neither an instruction nor any events, filter.
                if ix_result.is_none() && anchor_events.is_empty() {
                    return Err(ParseError::Filtered);
                }

                Ok(AnchorEventOutput {
                    instruction: ix_result,
                    anchor_events,
                })
            }
        }
    };
}

impl<InstructionParser, EventParser> Parser
    for AnchorEventInstructionParser<InstructionParser, EventParser>
where
    InstructionParser: Parser<Input = InstructionUpdate> + Send + Sync,
    EventParser: Parser<Input = InstructionUpdate> + Send + Sync,
    InstructionParser::Output: Send,
    EventParser::Output: Send,
{
    type Input = InstructionUpdate;
    type Output = AnchorEventOutput<InstructionParser::Output, EventParser::Output>;

    impl_parser!();
}

impl<InstructionParser, EventParser> ProgramParser
    for AnchorEventInstructionParser<InstructionParser, EventParser>
where Self: Parser
{
    fn program_id(&self) -> Pubkey { self.program_id }
}
